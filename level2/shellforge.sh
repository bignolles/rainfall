#!/bin/sh

if [ ! -z "$1" ]
then
	hexdump $1\
	| sed 's/[0-9a-f]\{7,8\} \?//'\
	| xargs
	echo "_________________________________________________________________"
	echo ""
	hexdump $1\
	| sed 's/[0-9a-f]\{7,8\} \?//'\
	| xargs\
	| sed 's/\([0-9a-f]\{2\}\)\([0-9a-f]\{2\}\)/\\x\2\\x\1/g'\
	| tr -d " "
fi
